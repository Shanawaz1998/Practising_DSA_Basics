//swapping using the third variable
public class P9 {

	public static void main(String[] args) {
		System.out.println("Before swapping");
		int a = 10;
		int b = 23;
		System.out.println(a+" "+b+" ");
		
		System.out.println("After swapping");
		int c = b;
		b=a;
		a=c;
		System.out.println(a+" "+b+" ");
		
		
	}

}
